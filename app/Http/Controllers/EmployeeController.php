    private function dataTable()
    {
        $query = User::query();  
            return DataTables::of($query)
                       ->order(function ($user)
                       {
                        $user->orderBy('created_at' , 'desc');
                       })
                       ->addColumn('created_at' , function ($data)
                      {
                        return date('d-M-Y H:i:s' , strtotime($data->created_at));
                      })
                       ->make(true);
    }

